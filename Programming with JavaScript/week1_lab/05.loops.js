
// For loop
console.log("For Loop:");
console.log("Now ascending:");
for (let i = 0; i < 10; i++) {
	console.log(i);
}
console.log("Now descending:");
for (let index = 9; index >= 0; index--) {
    console.log(index)
}

console.log("-------------------");
// While loop
console.log("While Loop:");
console.log("Now ascending:");
var b = 0;
while (b < 10) {
  console.log(b);
  b++;
}
console.log("Now descending:");
var b = 9;
while (b >= 0) {
  console.log(b);
  b--;
}

